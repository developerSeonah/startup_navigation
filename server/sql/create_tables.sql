-- MBTI 질문 테이블 생성
CREATE TABLE IF NOT EXISTS mbti_questions (
    question_id INT PRIMARY KEY,
    question_text TEXT NOT NULL,
    category VARCHAR(10) NOT NULL,
    weight DECIMAL(3,1) NOT NULL
);

-- MBTI 응답 테이블 생성
CREATE TABLE IF NOT EXISTS mbti_responses (
    response_id INT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(50) NOT NULL,
    question_id INT NOT NULL,
    score INT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (question_id) REFERENCES mbti_questions(question_id)
);

-- MBTI 결과 테이블 생성
CREATE TABLE IF NOT EXISTS mbti_result (
    mbti_type VARCHAR(4) PRIMARY KEY,
    mbti_name VARCHAR(50) NOT NULL,
    description TEXT NOT NULL
);

